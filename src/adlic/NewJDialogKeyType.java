    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Object[] row = { null };
            this.jModelType.addRow(row);
            this.jModelType.fireTableDataChanged();
            this.jTable1.requestFocus(); 
            this.jTable1.changeSelection(this.jTable1.getRowCount()-1,0,false, false);
            this.jLabel1.setText("Новая строка добавлена. Введите текст.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int a = this.jTable1.getSelectedRow();        
            this.jModelType.removeRow(a);
            a++;        
            this.jLabel1.setText("Строка № "+a+" удалена");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            this.jLabel1.setText("Сохранено...");
            licenses.getElementKeyType().removeContent();
            try {            
                for(int i=0;i<this.jModelType.getRowCount();i++) {                    
                    if (this.jTable1.getValueAt(i, 0) == null) {this.jLabel1.setText("Внимание! Пустые строки в списке не сохраняются");}                
                    else {licenses.getElementKeyType().addContent(new Element("type").addContent(this.jModelType.getValueAt(i, 0).toString()));}
                }             
            } catch (NullPointerException ex) {                
                JOptionPane.showMessageDialog(rootPane, ex);
            }
            try {
                    XMLOutputter outputter = new XMLOutputter();                    
                    outputter.setFormat(Format.getPrettyFormat());
                    try {
                        OutputStreamWriter out=new java.io.OutputStreamWriter(new java.io.FileOutputStream(licenses.getFile()),"UTF-8");
                        out.write(outputter.outputString(licenses.getDocument()));
                        out.close();
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(rootPane, ex);
                    }
            } catch (Exception ex) {            
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

