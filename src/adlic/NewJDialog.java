    /**
     * @param numlic - текущий номер елемента "license"
     */
    public void openDialog(int numlic) {        
        jTextField1.setText(licenses.getAttribute(numlic, "id"));
        jTextField2.setText(licenses.getProduct(numlic));
        jTextField3.setText(licenses.getType(numlic));
        jTextField4.setText(licenses.getNumberLic(numlic));
        jTextField5.setText(licenses.getAttribute(numlic, "key"));
        jTextField6.setText(licenses.getAttribute(numlic, "sum"));        
    }
    
     /**
     * @param numlic текущий номер елемента "license"
     */
    public void getTableKey(int numlic) {
        jTable1.setModel(jModelKey);        
        for (int i=0; i < licenses.getListKey(numlic).size(); i++) {  
            keynum = licenses.getElementProductKey(numlic, i);
            Object[] row = { keynum.getAttributeValue("name"), keynum.getAttributeValue("type"), keynum.getText()};
            jModelKey.addRow(row);            
        }
    }
    
    /**
     * @param numlic текущий номер елемента "license"
     */
    public void getTableSum(int numlic) {        
        jTable2.setModel(jModelSum);
        for (int i=0; i < licenses.getListSum(numlic).size(); i++) {  
            inventnum = licenses.getElementInventSum(numlic, i);
            Object[] row = { inventnum.getAttributeValue("type"), inventnum.getText()};
            jModelSum.addRow(row);
        }
        jTextField7.setText(new Integer(licenses.getListSum(numlic).size()).toString());
        int a = new Integer(licenses.getElementLicense(numlic).getAttributeValue("sum")).intValue();
        jTextField8.setText(new Integer(a - licenses.getListSum(numlic).size()).toString());        
    }
        
