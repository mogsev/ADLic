    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            licenses.getElementLicense(numlicsave).setAttribute("id", this.jTextField1.getText());
            licenses.getElementLicense(numlicsave).setAttribute("key", this.jTextField5.getText());
            licenses.getElementLicense(numlicsave).setAttribute("sum", this.jTextField6.getText());
            licenses.getElementLicense(numlicsave).getChild("product").setText(this.jTextField2.getText());
            licenses.getElementLicense(numlicsave).getChild("type").setText(this.jTextField3.getText());
            licenses.getElementLicense(numlicsave).getChild("numberlic").setText(this.jTextField4.getText());
            try {
                if (jModelKey.getRowCount()>(new Integer(this.jTextField5.getText()))) {
                    JOptionPane.showMessageDialog(rootPane, "Количество ключей в таблице превышает количество активных ключей.");
                } else {
                    licenses.getElementKey(numlicsave).removeContent();                
                    for(int i=0;i<this.jModelKey.getRowCount();i++) {                      
                        if ((this.jModelKey.getValueAt(i, 0) == null) 
                            && (this.jModelKey.getValueAt(i, 1) == null) 
                            && (this.jModelKey.getValueAt(i, 2) == null)) 
                        {JOptionPane.showMessageDialog(rootPane, "Пустые строки в таблице ключей. Заполните все поля.");}                
                        else {
                            if (this.jModelKey.getValueAt(i, 0) == null) {
                                this.jModelKey.setValueAt(" ", i, 0);
                                JOptionPane.showMessageDialog(rootPane, "Пустое поле <Название> в таблице ключей. Заполните поле.");
                            }
                            if (this.jModelKey.getValueAt(i, 1) == null) {
                                this.jModelKey.setValueAt(" ", i, 1);
                                JOptionPane.showMessageDialog(rootPane, "Пустое поле <Тип> в таблице ключей. Заполните поле.");
                            }
                            if (this.jModelKey.getValueAt(i, 2) == null) {
                                this.jModelKey.setValueAt(" ", i, 2);
                                JOptionPane.showMessageDialog(rootPane, "Пустое поле <Ключ> в таблице ключей. Заполните поле.");
                            }
                            licenses.getElementKey(numlicsave).addContent(new Element("product-key").addContent(this.jModelKey.getValueAt(i, 2).toString()));                                                
                            licenses.getElementProductKey(numlicsave, i).setAttribute("name", this.jModelKey.getValueAt(i, 0).toString());                        
                            licenses.getElementProductKey(numlicsave, i).setAttribute("type", this.jModelKey.getValueAt(i, 1).toString());                        
                        }
                    }
                }
            } catch (NullPointerException ex) {                
                JOptionPane.showMessageDialog(rootPane, ex);
            }
            try {
                if (jModelSum.getRowCount()>(new Integer(this.jTextField6.getText()))) {
                    JOptionPane.showMessageDialog(rootPane, "Количество техники превышает количество активных лицензий.");
                } else {
                    licenses.getElementSum(numlicsave).removeContent();
                    for(int i=0;i<this.jModelSum.getRowCount();i++) {                    
                        if ((this.jModelSum.getValueAt(i, 0) == null) 
                            && (this.jModelSum.getValueAt(i, 1) == null)) 
                            {JOptionPane.showMessageDialog(rootPane, "Пустые строки в таблице техники. Заполните все поля.");}
                        else {
                            if (this.jModelSum.getValueAt(i, 0) == null) {
                                this.jModelSum.setValueAt(" ", i, 0);
                                JOptionPane.showMessageDialog(rootPane, "Пустое поле <Название устройства> в таблице техники. Заполните поле.");
                            }
                            if (this.jModelSum.getValueAt(i, 1) == null) {
                                this.jModelSum.setValueAt(" ", i, 1);
                                JOptionPane.showMessageDialog(rootPane, "Пустое поле <Инвентарный номер> в таблице техники. Заполните поле.");
                            }
                            licenses.getElementSum(numlicsave).addContent(new Element("invent-number").addContent(this.jModelSum.getValueAt(i, 1).toString()));
                            licenses.getElementInventSum(numlicsave, i).setAttribute("type", this.jModelSum.getValueAt(i, 0).toString());                        
                        }
                    }
                }
            } catch (NullPointerException ex) {                
                JOptionPane.showMessageDialog(rootPane, ex);
            }
            try {
                    XMLOutputter outputter = new XMLOutputter();                    
                    outputter.setFormat(Format.getPrettyFormat());
                    try {
                        OutputStreamWriter out=new java.io.OutputStreamWriter(new java.io.FileOutputStream(licenses.getFile()),"UTF-8");
                        out.write(outputter.outputString(licenses.getDocument()));
                        out.close();
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(rootPane, ex);
                    }
            } catch (Exception ex) {            
                JOptionPane.showMessageDialog(rootPane, ex);
            } 
            this.jLabel5.setText("Изменения сохранены.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }        
    }//GEN-LAST:event_jButton2ActionPerformed

